const mongoose=require('mongoose')

const schema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String
    },
    password:{
        type:String,
        required:true
    },
    type:{
        type :String
    }
},{timestamps:true})

const admin= mongoose.model("admin",schema)
module.exports=admin















































































































































































































































